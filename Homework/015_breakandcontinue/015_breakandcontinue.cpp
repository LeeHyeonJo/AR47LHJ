int main()
{
    for (int i = 0; i < 100; i++)
    {
        if (0 == i%2) // 나머지를 활용해서 홀수만 나오게 한 조건임 
        {
            // 스위치의 브레이크와 다름~~~~
            // 반복문에서의 브레이커는 가장 위에서 가까운 반복문 1개를 탈출한다. -> 여기선 for문에서 탈출함.  
            continue; // 조건에 따라서 if문만 계속 반복해서 출력하게 해주는 것 
        }
        printf_s("%d\n", i);
    }
}
